var idade1 = Number(prompt("Digite a idade do do primeiro:"))
var idade2 = Number(prompt("Digite a idade do do primeiro:"))

if(idade1 == idade2){
    alert("Os dois tem a mesma idade!")
}else if(idade1 > idade2){
    alert("O primeiro é mais velho que o segundo!")
}else{
    alert("O segundo é mais velho que o primeiro!")
}