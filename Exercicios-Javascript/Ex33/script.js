var numero = Number(prompt("Digite um número e descubra se ele é Positivo, Negativo ou Zero"))

if(numero > 0) {
    alert("O Número digitado é Positivo. :)")
}
else if(numero < 0) {
    alert("O Número digitado é Negativo. :(")
}
else {
    alert("O Número digitado é Zero (Nem positivo Nem negativo)")
}