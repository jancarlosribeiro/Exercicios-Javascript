var primeiro = Number(prompt("Digite o Primeiro número"))
var segundo = Number(prompt("Digite o Segundo número"))
var terceiro = Number(prompt("Digite o Terceiro número"))
var quarto = Number(prompt("Digite o Quarto número"))

var resultado = (primeiro + segundo + terceiro + quarto) / 4

alert(`A média simples de ->${primeiro}, ->${segundo}, ->${terceiro}, ->${quarto}" é  "${resultado}" ;)`)